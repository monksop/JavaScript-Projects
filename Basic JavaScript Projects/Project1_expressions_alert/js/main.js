// Utilize the window.alert() and document.write() methods
var myString = "Hello, User!";
window.alert(myString);

// Create two variables and concatenate them
var Sent1 = "This is the beginning of the string";
var Sent2 = " and this is the end of the string";
var concatenatedSentence = Sent1 + Sent2;
document.write(concatenatedSentence + "<br>");

// Write an expression
var x = 10;
var y = 5;
var result = x + y * 2;

// Display the result of the expression using document.write()
document.write("The result of the expression x + y * 2 is: " + result + "<br>");
